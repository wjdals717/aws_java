package ch03_연산자;

public class Operator01 {
	public static void main(String[] args) {
		
		int num = 10;
		System.out.println(num++);
		System.out.println(num++);
		System.out.println(num);
		System.out.println(--num);
		System.out.println(num--);
		System.out.println(num);
		System.out.println(num++);
		System.out.println(num);

//		for(int i = 0; i<10; i++) 		//반복문에서는 ++i와 i++는 같음
		
		
		
	}
}
